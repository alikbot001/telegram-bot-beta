let quotesQueue = [];
function shuffleQuotes() {
    quotesQueue = MOTIVATION_QUOTES
        .map(q => ({ q, sort: Math.random() }))
        .sort((a, b) => a.sort - b.sort)
        .map(({ q }) => q);
}
function getNextQuote() {
    if (quotesQueue.length === 0) shuffleQuotes();
    return quotesQueue.pop();
}

